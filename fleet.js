function showNav(){
    if(document.getElementById("nestedNav").style.display==="block"){
        document.getElementById("nestedNav").style.display="none";
    }
    else{
        document.getElementById("nestedNav").style.display="block";
    }
}

function hideShow(){
    if(document.querySelector('.hide').style.display==="none")
    {
        document.querySelector('.hide').style.display="block";
    }
    else{
        document.querySelector('.hide').style.display="none";
    }
}